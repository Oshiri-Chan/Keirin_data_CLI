class KeirinMappings:
    """
    競輪関連のマッピングを提供するクラス
    """

    @staticmethod
    def get_venue_name(venue_code):
        """
        競輪場コードから競輪場名を取得する

        Args:
            venue_code (str): 競輪場コード

        Returns:
            str: 競輪場名。コードが見つからない場合は'不明'を返す
        """
        return VENUE_MAPPING.get(venue_code, "不明")

    @staticmethod
    def get_venue_code(venue_name):
        """
        競輪場名から競輪場コードを取得する

        Args:
            venue_name (str): 競輪場名

        Returns:
            str: 競輪場コード。名前が見つからない場合はNoneを返す
        """
        for code, name in VENUE_MAPPING.items():
            if name == venue_name:
                return code
        return None
